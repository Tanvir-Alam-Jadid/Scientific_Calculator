    private void jb34ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb34ActionPerformed
        second = Double.parseDouble(Display.getText());
        if(operation == "+")
        {
                result = first + second;
                ans= String.format("%.2f", result);
                Display.setText(ans);               
        }
        else if(operation == "-")
        {
                result = first - second;
                ans= String.format("%.2f", result);
                Display.setText(ans);               
        }
        else if(operation == "*")
        {
                result = first * second;
                ans= String.format("%.2f", result);
                Display.setText(ans);               
        }
        else if(operation == "/")
        {
                result = first / second;
                ans= String.format("%.2f", result);
                Display.setText(ans);               
        }
        else if(operation == "%")
        {       
                result = (first/second) * 100;
                ans= String.format("%.2f", result);
                Display.setText(ans);               
        }
        else if(operation == "X^Y")
        {
            double resultt=1;
            for (int i=0;i<second;i++)
            {
            resultt = first*resultt;
            }
            ans= String.format("%.2f", resultt);
                Display.setText(ans);
        }
        
        jLabel1.setText("");
    }//GEN-LAST:event_jb34ActionPerformed

